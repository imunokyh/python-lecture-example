"""
range 함수 활용하여 숫자 출력
"""

for i in range(10) :
    print(i, end =" ")
    
print()                         # 줄바꿈
for i in range(1, 11) :         
    print(i, end =" ")
    
print()                         # 줄바꿈
for i in range(1, 10, 2) :
    print(i, end =" ")
    
print()                         # 줄바꿈
for i in range(10, 0, -2) :
    print(i, end =" ")
